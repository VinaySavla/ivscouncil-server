const SECOND = 1000;
const SECONDS = SECOND;

const MINUTE = 60 * SECONDS;
const MINUTES = MINUTE;

const HOUR = 60 * MINUTES;
const HOURS = HOUR;

const DAY = 24 * HOURS;
const DAYS = DAY;

const MONTH = 30 * DAYS;
const MONTHS = MONTH;

const YEAR = 365 * DAYS;
const YEARS = YEAR;

module.exports = {
    SECOND, SECONDS,
    MINUTE, MINUTES,
    HOUR, HOURS,
    DAY, DAYS,
    MONTH, MONTHS,
    YEAR, YEARS
}
